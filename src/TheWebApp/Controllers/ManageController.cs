        //
        // POST: /Manage/ChangePasswordJson
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<JsonResult> ChangePasswordJson([FromBody]ChangePasswordViewModel model)
        {
            dynamic response = MakeInitialStatusResponse();
            if (!ModelState.IsValid)
            {
                return Json(response);
            }

            var user = await GetCurrentUserAsync();
            if (user == null)
            {
                return Json(response);
            }

            var result = await _userManager.ChangePasswordAsync(user, model.OldPassword, model.NewPassword);
            if (result.Succeeded)
            {
                await _signInManager.SignInAsync(user, isPersistent: false);
                _logger.LogInformation(3, "User changed their password successfully.");
                response.status.ok = true;
                return Json(response);
            }

            dynamic errors = new List<dynamic>();
            foreach (var error in result.Errors)
            {
                errors.Add(error.Description);
            }
            response.status.errors = errors;
            return Json(response);
        }

        //GET: /Manage/ManageLoginsJson
        [HttpGet]
        public async Task<JsonResult> ManageLoginsJson()
        {
            dynamic response = MakeInitialStatusResponse();

            var user = await GetCurrentUserAsync();
            if (user == null)
            {
                return Json(response);
            }
            var userLogins = await _userManager.GetLoginsAsync(user);
            var otherLogins = _signInManager.GetExternalAuthenticationSchemes().Where(auth => userLogins.All(ul => auth.AuthenticationScheme != ul.LoginProvider)).ToList();
           
            dynamic mlvResponse = new ManageLoginsViewModel
            {
                CurrentLogins = userLogins,
                OtherLogins = otherLogins
            };
            response.manageLoginsViewModel = mlvResponse;
            response.status.ok = true;
            return Json(response);
        }

        //
        // POST: /Manage/RiotLinkLogin
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> RiotLinkLogin(string provider)
        {
            // Clear the existing external cookie to ensure a clean login process
            await HttpContext.Authentication.SignOutAsync(_externalCookieScheme);

            // Request a redirect to the external login provider to link a login for the current user
            var redirectUrl = Url.Action(nameof(RiotLinkLoginCallback), "Manage");
            var properties = _signInManager.ConfigureExternalAuthenticationProperties(provider, redirectUrl, _userManager.GetUserId(User));
            return Challenge(properties, provider);
        }

        //
        // GET: /Manage/RiotLinkLoginCallback
        [HttpGet]
        public async Task<ActionResult> RiotLinkLoginCallback()
        {
            var user = await GetCurrentUserAsync();
            if (user == null)
            {
                return View("Error");
            }
            var info = await _signInManager.GetExternalLoginInfoAsync(await _userManager.GetUserIdAsync(user));
            if (info == null)
            {
                return new RedirectResult("/RiotAccount#account/manage-external-logins");
            }
            var result = await _userManager.AddLoginAsync(user, info);
            var message = ManageMessageId.Error;
            if (result.Succeeded)
            {
                message = ManageMessageId.AddLoginSuccess;
                // Clear the existing external cookie to ensure a clean login process
                await HttpContext.Authentication.SignOutAsync(_externalCookieScheme);
            }
            else
            {
                var errors = new List<string>();
                foreach (var error in result.Errors)
                {
                    errors.Add(error.Code);
                }
                var json = JsonConvert.SerializeObject(errors);

                return new RedirectResult("/RiotAccount#account/manage-external-logins?errors="+json);
            }
            return new RedirectResult("/RiotAccount#account/manage-external-logins");
        }

